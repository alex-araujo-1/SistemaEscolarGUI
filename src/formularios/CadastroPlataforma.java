    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if (!CampoVazio()) {

            Aluno aluno = new Aluno();
            AlunoCRUD dao = new AlunoCRUD();

            boolean status;
            int resposta;

            aluno.setNomeAluno(txtNome.getText());
            aluno.setCpf(txtCPF.getText());
            aluno.setEmail(txtEmail.getText());
            aluno.setTelefone(Integer.parseInt(txtTelefone.getText()));
            aluno.setEndereco(txtEndereco.getText());
            aluno.setNascimento(txtNascimento.getText());
            status = dao.conectar();

            if (status == false) {
                JOptionPane.showMessageDialog(null, "Erro de conex√£o");
            } else {
                resposta = dao.cadastrar(aluno);
                if (resposta == 1) {
                    JOptionPane.showMessageDialog(null, "Dados incluidos com sucesso");

                    txtNome.setText("");
                    txtCPF.setText("");
                    txtEmail.setText("");
                    txtTelefone.setText("");
                    txtEndereco.setText("");
                    txtNascimento.setText("");

                    txtNome.requestFocus();

                    dispose();
                    TelaTipoUsuario tu = new TelaTipoUsuario();
                    tu.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
                }
                dao.desconectar();
            }

        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

